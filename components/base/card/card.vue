<template>
  <div :class="classes" :style="stylesWrap">
    <h3 v-if="title" class="z-card-header">{{ title }}</h3>
    <div class="z-card-body" :style="stylesBody">
      <slot></slot>
    </div>
  </div>
</template>

<script>
const prefixCls = 'z-card';

export default {
  name: 'Card',
  props: {
    title: {
      type: String,
      default: '',
    },
    hover: Boolean,
    shadow: Boolean,
    padding: {
      type: [String, Number],
      default: '15px',
    },
    borderRadius: {
      type: [String, Number],
      default: '5px',
    },
  },
  computed: {
    classes() {
      return [
        `${prefixCls}`,
        {
          [`${prefixCls}-hover`]: this.hover,
          [`${prefixCls}-shadow`]: this.shadow,
        },
      ];
    },
    stylesWrap() {
      return {
        borderRadius: typeof this.borderRadius === 'number' ? `${this.borderRadius}px` : this.borderRadius,
      };
    },
    stylesBody() {
      return {
        padding: typeof this.padding === 'number' ? `${this.padding}px` : this.padding,
      };
    },
  },
};
</script>

<style lang="less" scoped>
.z-card {
  background-color: #fff;
  margin-bottom: 20px;
  overflow: hidden;
  transition: all 0.2s linear;
  box-shadow: 0 1px 1px rgba(0, 0, 0, 0.1);

  &-shadow {
    box-shadow: 0 1px 1px rgba(0, 0, 0, 0.1);
  }
  &-hover:hover {
    transform: translate3d(0, -2px, 0);
    box-shadow: 0 15px 30px rgba(0, 0, 0, 0.1);
  }
  &-header {
    font-size: 18px;
    line-height: 50px;
    text-align: center;
    border-bottom: 1px solid @colorBorderLight;
    color: @colorTextTitle;
    font-weight: normal;
  }
  &-body {
    height: 100%;
  }
}
</style>
